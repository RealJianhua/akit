package wenjh.akit.demo.people.model;

public class UserAge {
	
	private int age;
	
	private int showOrNotShow; // 0:hide 1:show
	
	public int getShowOrNotShow() {
		return showOrNotShow;
	}

	public void setShowOrNotShow(int showOrNot) {
		this.showOrNotShow = showOrNot;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	

}
